package day6.loops;

public class Example4 {

	public static void main(String[] args) {
		int num=5;
		System.out.println("*********Using while **********");
		while(num>20) {
			System.out.println(num);
		}
		System.out.println("**********Using do-while ********");
		do {
			System.out.println(num);
		}while(num>20);

	}

}
